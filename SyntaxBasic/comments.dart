//Dart supports single-line comments, multi-line comments, and documentation comments.
// Single-line comments start with // and continue until the end of the line.
// Multi-line comments start with /* and end with */.

/* 
this is a multi-line comment
it can span multiple lines
*/

/// Documentation comments start with /// and are used to generate documentation for the code.
/// For example:

/// This function adds two numbers.
/// [a] is the first number and [b] is the second number.

int add(int a, int b) {
  return a + b;
}

// So the documentation comments are used to generate documentation for the code.
