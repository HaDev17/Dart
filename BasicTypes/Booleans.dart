// Booleans in Dart
// Booleans are used to represent true or false values.
// Dart has a built-in boolean data type called bool.
// The bool data type can have only two values: true or false.
//
// // Example: Booleans in Dart
var isTrue = true;
var isFalse = false;

// or you can use the bool keyword to declare a boolean variable
bool isTrue1 = true;
bool isFalse1 = false;

// empty string, zero, null, and NaN are considered as false
