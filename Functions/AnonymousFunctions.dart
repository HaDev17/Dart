// Anonymous Functions in Dart

// Anonymous functions are functions without a name. They are also known as lambda or closure functions.

// Simple example :
var anonymous = () {
  print("This is an anonymous function");
};

// Anonymous functions are like Named functions, but they don't have a name.
// They are used when you need a function for a short period of time.

void main() {
  anonymous(); // Output: This is an anonymous function
}
